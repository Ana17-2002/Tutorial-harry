package com.company;

import java.util.Scanner;

public class cwh_ch_5_21 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("enter the value of n :  ");
        int n = sc.nextInt();

        int i = 0;
        while (i <= n) {
            System.out.println(i);
            i++;
        }
    }
}
